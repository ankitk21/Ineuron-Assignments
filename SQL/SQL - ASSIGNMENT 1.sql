CREATE TABLE PHONES(
NAME VARCHAR(20) NOT NULL UNIQUE,
PH_NO INT NOT NULL UNIQUE)

CREATE TABLE CALLS(
ID INT NOT NULL UNIQUE,
CALLER INT NOT NULL,
CALLEE INT NOT NULL,
DURATION INT NOT NULL
)

INSERT INTO PHONES VALUES
